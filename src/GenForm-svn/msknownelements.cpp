				EI.m_vecStateLon.push_back(pAtomStates[StateIdx][1]);
				EI.m_vecStateRad.push_back(pAtomStates[StateIdx][2]);
				EI.m_vecStateCha.push_back(pAtomStates[StateIdx][3]);
